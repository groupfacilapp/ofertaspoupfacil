-- --------------------------------------------------------------------
-- 2.1 planos_sistema
-- Tabela de planos disponíveis na plataforma (trial, básico, etc.)
-- --------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.planos_sistema (
    id                  INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    slug                TEXT NOT NULL UNIQUE,
    nome                TEXT NOT NULL,
    descricao           TEXT,
    valor               NUMERIC NOT NULL DEFAULT 0,
    tipo_periodo        TEXT NOT NULL DEFAULT 'free',
    link_checkout       TEXT NOT NULL DEFAULT '',
    recursos            JSONB NOT NULL DEFAULT '[]'::jsonb,
    ativo               BOOLEAN NOT NULL DEFAULT true,
    destaque            BOOLEAN NOT NULL DEFAULT false,
    ordem_exibicao      INTEGER NOT NULL DEFAULT 1,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    max_grupos          INTEGER,
    max_marketplaces    INTEGER,
    max_disparos_dia    INTEGER,
    max_destinos_grupo  INTEGER,
    periodo_dias        INTEGER,
    valor_mensal_equiv  NUMERIC,

    CONSTRAINT planos_sistema_tipo_periodo_check
        CHECK (tipo_periodo = ANY (ARRAY['free','mensal','trimestral','semestral','anual'])),
    CONSTRAINT planos_sistema_ordem_exibicao_check
        CHECK (ordem_exibicao > 0)
);


-- --------------------------------------------------------------------
-- 2.2 platform_settings
-- Configurações globais da plataforma (Evolution API, limites, etc.)
-- --------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.platform_settings (
    key         TEXT PRIMARY KEY,
    value       TEXT NOT NULL DEFAULT '',
    description TEXT,
    updated_at  TIMESTAMPTZ DEFAULT now()
);


-- --------------------------------------------------------------------
-- 2.3 users
-- Perfis de usuários (espelha auth.users do Supabase)
-- --------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.users (
    id           UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
    display_name TEXT,
    avatar_url   TEXT,
    created_at   TIMESTAMPTZ DEFAULT now(),
    updated_at   TIMESTAMPTZ DEFAULT now()
);


-- --------------------------------------------------------------------
-- 2.4 user_plans
-- Plano ativo de cada usuário e contadores de uso
-- --------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.user_plans (
