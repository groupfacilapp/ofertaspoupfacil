-- Interface RPC do Coordinator (acesso externo via supabaseAdmin)
CREATE OR REPLACE FUNCTION public.dz_enqueue_fetch(job jsonb) RETURNS bigint
LANGUAGE sql SECURITY DEFINER SET search_path TO 'public' AS $$
  SELECT pgmq.send('fetch_jobs', job);
$$;

CREATE OR REPLACE FUNCTION public.dz_enqueue_dispatch(job jsonb) RETURNS bigint
LANGUAGE sql SECURITY DEFINER SET search_path TO 'public' AS $$
  SELECT pgmq.send('dispatch_jobs', job);
$$;

CREATE OR REPLACE FUNCTION public.dz_dequeue_dispatch(batch_size integer DEFAULT 10)
RETURNS TABLE(msg_id bigint, read_ct integer, enqueued_at timestamptz, vt timestamptz, message jsonb)
LANGUAGE sql SECURITY DEFINER SET search_path TO 'public' AS $$
  SELECT msg_id, read_ct, enqueued_at, vt, message
  FROM pgmq.read('dispatch_jobs', 120, batch_size);
$$;

CREATE OR REPLACE FUNCTION public.dz_dequeue_fetch(batch_size integer DEFAULT 5)
RETURNS TABLE(msg_id bigint, read_ct integer, enqueued_at timestamptz, vt timestamptz, message jsonb)
LANGUAGE sql SECURITY DEFINER SET search_path TO 'public' AS $$
  SELECT msg_id, read_ct, enqueued_at, vt, message
  FROM pgmq.read('fetch_jobs', 300, batch_size);
$$;

CREATE OR REPLACE FUNCTION public.dz_complete_dispatch(job_id bigint) RETURNS boolean
LANGUAGE sql SECURITY DEFINER SET search_path TO 'public' AS $$
  SELECT pgmq.delete('dispatch_jobs', job_id);
$$;

CREATE OR REPLACE FUNCTION public.dz_complete_fetch(job_id bigint) RETURNS boolean
LANGUAGE sql SECURITY DEFINER SET search_path TO 'public' AS $$
  SELECT pgmq.delete('fetch_jobs', job_id);
$$;

CREATE OR REPLACE FUNCTION public.dz_queue_metrics()
RETURNS TABLE(queue_name text, queue_length bigint, oldest_msg_age_sec integer, total_messages bigint)
LANGUAGE sql SECURITY DEFINER SET search_path TO 'public' AS $$
  SELECT queue_name, queue_length, oldest_msg_age_sec, total_messages
  FROM pgmq.metrics_all();
$$;


