-- --------------------------------------------------------------------
-- 2.12 dispatch_logs
-- Histórico de disparos realizados
-- offer_id é nullable: quando um offer expira e é deletado, o log é preservado
-- mas offer_id vira NULL (ON DELETE SET NULL).
-- --------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.dispatch_logs (
    id                  UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id             UUID NOT NULL,
    group_id            UUID NOT NULL,
    offer_id            UUID,  -- nullable: SET NULL quando offer é deletado
    channel_type        TEXT NOT NULL,
    status              TEXT NOT NULL DEFAULT 'pending',
    error_message       TEXT,
    external_message_id TEXT,
    attempt_count       INTEGER DEFAULT 1,
    dispatched_at       TIMESTAMPTZ DEFAULT now(),
    dispatched_date     DATE NOT NULL DEFAULT CURRENT_DATE,
    created_at          TIMESTAMPTZ DEFAULT now(),

    CONSTRAINT dispatch_logs_status_check
        CHECK (status = ANY (ARRAY['pending','sent','delivered','read','failed'])),
    CONSTRAINT dispatch_logs_user_id_fkey
        FOREIGN KEY (user_id) REFERENCES public.users(id) ON DELETE CASCADE,
    CONSTRAINT dispatch_logs_group_id_fkey
        FOREIGN KEY (group_id) REFERENCES public.dispatch_groups(id),
    CONSTRAINT dispatch_logs_offer_id_fkey
        FOREIGN KEY (offer_id) REFERENCES public.offers(id) ON DELETE SET NULL
);


