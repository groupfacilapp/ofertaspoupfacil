-- --------------------------------------------------------------------
-- 5.1 handle_new_user
-- Cria o perfil em public.users ao registrar novo usuário no auth
-- --------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = 'public'
AS $$
BEGIN
    INSERT INTO public.users (id, display_name)
    VALUES (
        new.id,
        COALESCE(new.raw_user_meta_data->>'display_name', split_part(new.email, '@', 1))
    );
    RETURN new;
END;
$$;

CREATE OR REPLACE TRIGGER on_auth_user_created
    AFTER INSERT ON auth.users
    FOR EACH ROW
    EXECUTE FUNCTION public.handle_new_user();


-- --------------------------------------------------------------------
-- 5.2 handle_new_user_plan
-- Cria plano trial (7 dias) ao registrar novo usuário no auth
-- --------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.handle_new_user_plan()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = 'public'
AS $$
DECLARE
    trial_plan_id INTEGER;
BEGIN
    SELECT id INTO trial_plan_id
    FROM public.planos_sistema
    WHERE slug = 'trial'
    LIMIT 1;

    INSERT INTO public.user_plans
        (user_id, plan, plan_id, plan_started_at, plan_expires_at)
    VALUES
        (new.id, 'trial', trial_plan_id, now(), now() + INTERVAL '7 days');

    RETURN new;
END;
$$;

CREATE OR REPLACE TRIGGER on_auth_user_created_plan
    AFTER INSERT ON auth.users
    FOR EACH ROW
    EXECUTE FUNCTION public.handle_new_user_plan();


